tokenizer = AutoTokenizer.from_pretrained(model_path)
model = AutoModelForSeq2SeqLM.from_pretrained(model_path)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model.to(device)
model.eval()


# -----------------------------
# Decode a single chunk (Top-1 beam only)
# -----------------------------
def decode_a_text(text):
    inputs = tokenizer(
        text,
        return_tensors="pt",
        truncation=True,
        max_length=256
    ).to(device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            num_beams=4,
            num_return_sequences=1,
            max_new_tokens=256,
            early_stopping=True,
        )

    decoded = tokenizer.decode(outputs[0], skip_special_tokens=True)
    return decoded


# -----------------------------
# Split into word chunks (24 words)
# -----------------------------
def split_into_word_chunks(text, chunk_size=24):
    words = text.split()
    return [
        " ".join(words[i:i+chunk_size])
        for i in range(0, len(words), chunk_size)
    ]


# -----------------------------
# Decode long text (24-word chunks)
# -----------------------------
def decode_large_text(full_text, chunk_size=12):

    chunks = split_into_word_chunks(full_text, chunk_size)
    print(f"\nTotal chunks: {len(chunks)}")
    print("=" * 50)

    decoded_all = []

    for idx, chunk in enumerate(chunks, 1):
        print(f"\n### Decoding chunk {idx}/{len(chunks)}")
        print(f"Chunk: {chunk}")

        best_decoded = decode_a_text(chunk)
        print(f"→ Decoded: {best_decoded}")

        decoded_all.append(best_decoded)

    final_output = " ".join(decoded_all).strip()
    return final_output


